"""
CROP RESPONSE TO SQ3 CLASSES:

Different crops have vastly different rooting requirements and responses to 
rooting conditions.

VERY HIGH ROOTING CONDITIONS (80-100):

Excellent for:
- Alfalfa and deep-rooted legumes (roots to 3+ meters)
- Sugar beet (deep taproot)
- Sunflower (deep roots)
- Tree crops (fruit and nut trees, oil palm)
- Grapevines (deep roots in dry climates)
- Safflower
- Deep-rooted forages

Very good for:
- All cereals (wheat, maize, sorghum, rice)
- Cotton
- Soybeans and grain legumes
- Most vegetables
- All root and tuber crops

Benefits:
- Maximum drought resistance
- Efficient nutrient scavenging
- Strong anchorage
- High yield stability
- Flexibility in irrigation scheduling


HIGH ROOTING CONDITIONS (60-79):

Excellent for:
- Cereals (wheat, maize, barley, oats)
- Grain legumes (soybeans, peas, beans, peanuts)
- Cotton
- Most vegetables
- Potatoes and shallow root crops
- Short-season perennials

Good for:
- Alfalfa (with irrigation)
- Deep-rooted crops with moderate requirements
- Tree crops (with supplemental water)

Minor limitations:
- Deepest-rooted perennials may need irrigation
- Tree crops may need supplemental feeding
- Some yield reduction in drought years


MEDIUM ROOTING CONDITIONS (40-59):

Suitable with good management:
- Cereals (may need irrigation)
- Grain legumes
- Cotton (with irrigation)
- Potatoes
- Most vegetables (with irrigation)
- Short-season crops

Requires careful management:
- Sunflower
- Sugar beet
- Sorghum

Limited suitability:
- Tree crops (only with irrigation and intensive care)
- Alfalfa (shallow varieties only, with irrigation)
- Deep-rooted perennials

Management needs:
- Irrigation often essential
- Frequent, light applications
- Efficient fertilizer placement
- May benefit from soil modification


LOW ROOTING CONDITIONS (20-39):

Limited to:
- Lettuce and leafy greens
- Onions and shallow-rooted vegetables
- Shallow-rooted herbs
- Short-season annual grasses
- Strawberries (if irrigated)

Requires intensive management:
- Frequent irrigation essential
- Very shallow water applications
- Surface fertilizer applications
- Mulching beneficial
- Drip irrigation ideal

Generally unsuitable:
- All deep-rooted crops
- Most cereals
- Perennial crops
- Crops requiring strong anchorage

Economic considerations:
- Only viable for high-value crops
- High input costs
- May require soil modification
- Limited crop rotation options


VERY LOW ROOTING CONDITIONS (0-19):

Extremely limited options:
- Moss, lichens, very shallow specialists
- Some succulents (if rock garden)
- Specialized adapted native plants

Essentially unsuitable for:
- Conventional agriculture
- Commercial crop production
- Most horticultural uses

Possible uses:
- Extensive grazing (very poor)
- Native vegetation
- Wildlife habitat
- Soil/rock modification needed for cropping


ROOTING DEPTH REQUIREMENTS BY CROP:

Very shallow rooted (<30 cm):
- Lettuce, spinach, radish
- Onions (bulb crops)
- Most leafy herbs
- Shallow-rooted flowers

Shallow rooted (30-60 cm):
- Potatoes, onions (bulb)
- Cabbage family
- Beans (bush types)
- Many vegetables
- Annual flowers

Medium rooted (60-100 cm):
- Wheat, barley, oats
- Maize (corn)
- Soybeans, peas
- Tomatoes
- Carrots (taproot)
- Cotton

Deep rooted (100-150 cm):
- Sorghum
- Sunflower
- Peanuts
- Sweet potato
- Many melons

Very deep rooted (>150 cm):
- Alfalfa (2-3 m+)
- Sugar beet (2 m+)
- Tree crops (3-6 m+)
- Grapevines (2-3 m+)
- Safflower (2 m+)
"""

