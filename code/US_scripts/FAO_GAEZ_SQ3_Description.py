"""
SQ3 INTERACTIONS WITH OTHER GAEZ INDICATORS:

SQ3 ↔ SQ1 (Nutrient Availability):
- Deep rooting allows access to deep nutrients
- Poor rooting limits nutrient uptake even if nutrients present
- Shallow soils leach nutrients beyond root zone
- Root volume determines nutrient mining capacity
- Restricting layers prevent access to subsoil nutrients
- Together determine effective nutrient supply

SQ3 ↔ SQ2 (Nutrient Retention Capacity):
- Good retention in subsoil only useful if roots can access
- Deep rooting captures leached nutrients
- Shallow rooting negates benefits of deep retention
- Root exploration matches retention zones
- Restricting layers limit effectiveness of deep CEC

SQ3 ↔ SQ4 (Oxygen Availability):
- Compaction reduces both rooting and aeration
- Poor drainage restricts both oxygen and roots
- High bulk density affects both factors
- Waterlogged zones prevent root penetration
- Structure affects both pore space for roots and air
- Dense layers often have both problems

SQ3 ↔ SQ5 (Excess Salts):
- Shallow rooting concentrates roots in salty surface
- Deep rooting allows avoidance of saline layers
- Restricting layers force roots into saline zones
- Good rooting conditions allow leaching of salts
- Salt accumulations often occur at restricting layers
- Poor drainage (affecting SQ4) causes salt accumulation

SQ3 ↔ SQ6 (Toxicity):
- Deep rooting may expose roots to toxic subsoil
- Shallow rooting may avoid deep toxicity
- Acid subsoils are both restrictive and toxic
- Aluminum toxicity restricts rooting
- Restricting layers and toxic layers often coincide
- Good rooting allows dilution of toxicity impacts

SQ3 ↔ SQ7 (Workability):
- Compacted soils have both poor rooting and poor workability
- Hard soils restrict both tillage and roots
- Rocky soils affect both equipment and roots
- Shallow soils limit both root depth and tillage options
- Heavy clays may have restricted roots despite deep profile
- Structure affects both factors
"""

# ============================================================================
# LIMITATIONS AND CONSIDERATIONS
# ============================================================================

"""
IMPORTANT LIMITATIONS OF SQ3:

1. Static representation:
   - Soil properties measured at one time
   - Doesn't capture seasonal changes
   - Moisture-dependent properties simplified
   - Biological root growth processes not modeled
   - Plant-soil feedbacks not included

2. Crop-generic ratings:
   - Standard rating may not fit all crops
   - Shallow-rooted crops rate soils differently than deep-rooted
   - Doesn't account for specific crop tolerances
   - Crop-specific adjustments needed for accuracy

3. Management effects not captured:
   - Recent tillage changes conditions
   - Compaction from traffic not necessarily mapped
   - Soil improvement over time not tracked
   - Historical management unknown

4. Scale issues:
   - Map unit may contain variability
   - Point measurements extrapolated
   - Local conditions may differ
   - Field-scale variation not captured

5. Restricting layer characterization:
   - Gradational restrictions hard to classify
   - Rootability of weathered rock variable
   - Fractures in rock not mapped
   - Temporal variation in restrictions (e.g., permafrost depth)

6. Structure assessment limitations:
   - Structure difficult to measure/map
   - Varies with moisture and management
   - Changes seasonally
   - Subjective interpretation

7. Biological factors simplified:
   - Root-soil organism interactions not included
   - Biopores from old roots not considered
   - Mycorrhizal effects not modeled
   - Earthworm channels not mapped

8. Missing factors:
   - Soil temperature affects rooting
   - Root diseases and pests not included
   - Allelopathy not considered
   - Root competition from weeds/other plants

RECOMMENDED USES:

Best for:
- Regional crop suitability assessment
- Identifying soils with rooting limitations
- Preliminary site evaluation
- Comparative assessment between areas
- Planning soil improvement needs
- Guiding crop selection at regional scale

Should be supplemented with:
- Site-specific soil investigation
- Penetrometer or bulk density measurements
- Soil pit examination
- Root observation studies
- Crop performance trials
- Local experience and knowledge
- Season-long observation of crop performance
"""

