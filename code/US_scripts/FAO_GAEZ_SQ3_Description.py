"""
FAO GAEZ SQ3: ROOTING CONDITIONS - DETAILED DESCRIPTION
========================================================

Based on the FAO Global Agro-Ecological Zones (GAEZ) methodology and the 
Harmonized World Soil Database (HWSD) framework.

Data Citation:
Fischer, G., F. Nachtergaele, S. Prieler, H.T. van Velthuizen, L. Verelst, D. Wiberg, 2008. 
Global Agro-ecological Zones Assessment for Agriculture (GAEZ 2008). 
IIASA, Laxenburg, Austria and FAO, Rome, Italy.
"""

# ============================================================================
# OVERVIEW
# ============================================================================

"""
SQ3: ROOTING CONDITIONS

SQ3 represents the physical conditions that affect root growth, penetration, 
and development throughout the soil profile. This soil quality indicator assesses 
the ease with which plant roots can explore the soil volume, access water and 
nutrients, and establish a functional root system that supports plant growth.

POSITION IN GAEZ FRAMEWORK:
SQ3 is one of seven key soil quality indicators used in the FAO GAEZ methodology:
- SQ1: Nutrient availability
- SQ2: Nutrient retention capacity
- SQ3: Rooting conditions (focus of this document)
- SQ4: Oxygen availability to roots
- SQ5: Excess salts
- SQ6: Toxicity
- SQ7: Workability/field management constraints
"""

# ============================================================================
# CONCEPTUAL BASIS
# ============================================================================

"""
FUNDAMENTAL CONCEPT:

Rooting conditions encompass the physical properties of soil that either 
facilitate or restrict root growth and function. Good rooting conditions are 
essential because roots:

1. Anchor plants and provide physical support
2. Explore soil volume for water and nutrients
3. Absorb water and dissolved nutrients
4. Store carbohydrates and other reserves
5. Synthesize hormones and other compounds
6. Interact with beneficial soil organisms
7. Contribute to soil structure through exudates and turnover

Physical barriers or limitations to rooting:
- Restrict the volume of soil that roots can explore
- Limit access to water and nutrients
- Reduce plant anchorage and stability
- Increase susceptibility to drought stress
- Decrease overall plant vigor and productivity
- Make plants more vulnerable to lodging

AGRICULTURAL RELEVANCE:

For all crops:
- Root depth determines effective soil volume
- Root distribution affects resource capture efficiency
- Root system size correlates with shoot growth
- Root health is fundamental to plant health
- Rooting restrictions often limit yield potential

Critical for:
- Deep-rooted perennial crops (trees, vines, alfalfa)
- Crops grown in water-limited environments
- Soils with nutrients concentrated in subsoil
- Areas with variable rainfall patterns
- Tap-rooted crops (carrots, beets, cassava)

Impact on management:
- Determines irrigation frequency and amount
- Affects fertilizer placement strategies
- Influences tillage depth requirements
- Determines crop selection possibilities
- Impacts erosion control effectiveness
"""

# ============================================================================
# SOIL CHARACTERISTICS EVALUATED
# ============================================================================

"""
SQ3 DIAGNOSTIC CHARACTERISTICS:

The assessment of rooting conditions focuses on physical properties that 
influence root penetration, growth, and function throughout the soil profile. 
These are evaluated across multiple depth increments.

----------------------------------
PROFILE DEPTH AND VOLUME (0-150 cm)
----------------------------------

The evaluation considers the entire potential rooting zone, typically to 150 cm 
depth, though effective rooting depth varies by crop:

Shallow-rooted crops (0-50 cm):
- Most vegetables
- Onions, lettuce
- Potatoes (tuber zone)
- Shallow-rooted grasses

Medium-rooted crops (50-100 cm):
- Cereals (wheat, maize, rice)
- Grain legumes (soybeans, beans)
- Cotton
- Most annual field crops

Deep-rooted crops (100-150+ cm):
- Alfalfa and deep-rooted legumes
- Sunflower
- Sorghum
- Sugar beet
- Many tree crops
- Grapevines


1. SOIL TEXTURE AND STRUCTURE
   
   Definition: The arrangement of soil particles and aggregates that creates 
   the physical framework through which roots must grow.
   
   Role in rooting conditions:
   - Determines mechanical resistance to root penetration
   - Affects pore size distribution for root accommodation
   - Influences water and air movement to roots
   - Controls ease of root elongation
   - Determines soil strength when dry or wet
   
   Texture effects on rooting:
   
   Sandy soils:
   - Low mechanical resistance
   - Easy root penetration when moist
   - Very hard when dry (single-grain structure)
   - Large pores may not contact roots well
   - Poor water retention limits rooting
   
   Loamy soils:
   - Moderate mechanical resistance
   - Good root penetration
   - Stable structure supports roots
   - Balanced pore sizes
   - Generally optimal for rooting
   
   Clay soils:
   - High mechanical resistance when dense
   - Can be excellent when well-structured
   - Swelling-shrinking creates cracks
   - High strength when dry
   - Plastic and sticky when wet
   - Structure quality critical
   
   Structure types and rooting:
   
   Granular/crumb (surface soils):
   - Excellent for seedling establishment
   - Easy root penetration
   - Good pore continuity
   - Typical of high organic matter soils
   
   Blocky/subangular blocky:
   - Good to excellent for rooting
   - Well-defined structural units
   - Adequate macropores between peds
   - Roots follow ped faces initially
   
   Prismatic/columnar:
   - Moderate rooting conditions
   - Vertical pores favor downward growth
   - May restrict lateral root spread
   - Common in subsoils
   
   Massive (structureless):
   - Poor to very poor for rooting
   - High mechanical resistance
   - Limited macropore space
   - Roots restricted to natural cracks
   - Common limitation in dense subsoils
   
   Platy:
   - Very poor for rooting
   - Horizontal orientation restricts downward growth
   - Often associated with compaction
   - Roots deflected horizontally


2. BULK DENSITY AND COMPACTION
   
   Definition: The mass of dry soil per unit volume, including pore space, 
   expressed as g/cm³ or Mg/m³.
   
   Role in rooting conditions:
   - Direct measure of soil compactness
   - Indicates pore space availability
   - Reflects mechanical resistance
   - Determines penetrability
   - Affects water and air movement
   
   Critical bulk density thresholds for root restriction:
   
   Sandy soils:
   - Ideal: <1.40 g/cm³
   - Moderate restriction: 1.40-1.65 g/cm³
   - Severe restriction: >1.65 g/cm³
   - Root growth essentially stops: >1.80 g/cm³
   
   Loamy soils:
   - Ideal: <1.30 g/cm³
   - Moderate restriction: 1.30-1.50 g/cm³
   - Severe restriction: >1.50 g/cm³
   - Root growth essentially stops: >1.70 g/cm³
   
   Clay soils:
   - Ideal: <1.10 g/cm³
   - Moderate restriction: 1.10-1.30 g/cm³
   - Severe restriction: >1.30 g/cm³
   - Root growth essentially stops: >1.50 g/cm³
   
   Compaction effects:
   - Reduces total porosity
   - Eliminates macropores preferentially
   - Increases mechanical resistance
   - Reduces water infiltration
   - Limits gas exchange
   - Concentrates roots in uncompacted zones
   
   Natural vs. anthropogenic compaction:
   
   Natural densification:
   - Soil development processes
   - Clay translocation (argillic horizons)
   - Cementation by carbonates, iron, silica
   - Usually gradual with depth
   
   Induced compaction:
   - Tillage pans (plow pans)
   - Traffic pans from equipment
   - Livestock trampling
   - Often at specific depths (15-30 cm)
   - Can be more severe than natural


3. SOIL DEPTH AND RESTRICTING LAYERS
   
   Definition: The vertical extent of soil suitable for rooting before 
   encountering a layer that severely limits or prevents root penetration.
   
   Types of restricting layers:
   
   LITHIC CONTACT (hard rock):
   - Coherent bedrock
   - Essentially impenetrable to roots
   - Absolute depth limitation
   - Examples: granite, basalt, limestone, sandstone
   
   PARALITHIC CONTACT (soft rock):
   - Weathered bedrock, can be penetrated by some roots
   - Severely restricts rooting
   - Examples: weathered shale, soft sandstone, saprolite
   
   PETROFERRIC/PETROCALCIC HORIZONS:
   - Cemented layers (iron, calcium carbonate, silica)
   - Very hard when dry
   - May fracture to allow some root penetration
   - Common in tropical and arid regions
   
   FRAGIPAN:
   - Dense, brittle subsurface horizon
   - High bulk density, platy structure
   - Restricts vertical root growth
   - Causes perched water tables
   - Common in temperate regions
   
   DENSIC/PLACIC LAYERS:
   - Compacted glacial till
   - Thin iron pans
   - Variable root restriction
   
   PERMAFROST:
   - Permanently frozen layer
   - Absolute rooting limitation
   - Variable depth seasonally
   
   Effective soil depth classes:
   
   Very deep (>150 cm):
   - No restrictions to deep rooting
   - Full exploration of profile
   - Suitable for all crops
   - Maximum drought buffering
   
   Deep (100-150 cm):
   - Adequate for most crops
   - Some limitation for deepest-rooted species
   - Good water storage capacity
   - Minor drought limitation
   
   Moderately deep (50-100 cm):
   - Suitable for shallow to medium-rooted crops
   - Limits deep-rooted perennials
   - Moderate water storage
   - Increased drought susceptibility
   
   Shallow (25-50 cm):
   - Severe limitations for most crops
   - Only shallow-rooted species suitable
   - Low water storage
   - High drought risk
   - Frequent irrigation needed
   
   Very shallow (<25 cm):
   - Extremely limited crop options
   - Only very shallow-rooted species
   - Minimal water storage
   - Very high management requirements


4. COARSE FRAGMENTS (ROCK FRAGMENTS)
   
   Definition: Particles >2 mm diameter, including gravel, stones, cobbles, 
   and boulders.
   
   Role in rooting conditions:
   - Reduce volume of fine earth available for rooting
   - Create physical barriers to root penetration
   - Affect water storage capacity
   - Influence soil workability
   - May improve or impede drainage
   
   Coarse fragment content classes:
   
   None to few (0-15% by volume):
   - Minimal impact on rooting
   - Full soil volume available
   - No significant barriers
   
   Common (15-35% by volume):
   - Moderate reduction in rooting volume
   - Some barriers to root elongation
   - Reduced water storage
   - May affect tillage
   
   Many (35-60% by volume):
   - Significant reduction in rooting volume
   - Substantial barriers present
   - Markedly reduced water storage
   - Tillage difficulties
   - Some crop limitations
   
   Abundant (60-90% by volume):
   - Severe reduction in rooting volume
   - Extensive physical barriers
   - Very limited water storage
   - Major crop limitations
   - Skeleton soils
   
   Dominant (>90% by volume):
   - Extremely limited fine earth
   - Nearly continuous rock
   - Minimal rooting possible
   - Very few crop options
   
   Fragment characteristics matter:
   
   Size:
   - Gravel (2-75 mm): less restrictive
   - Stones (75-250 mm): moderate restriction
   - Boulders (>250 mm): severe restriction
   
   Shape:
   - Rounded: less restrictive
   - Angular: more restrictive
   - Flat: most restrictive (platy)
   
   Orientation:
   - Random: moderate effect
   - Horizontal layers: more restrictive
   - Vertical orientation: less restrictive


5. SOIL CONSISTENCE AND PENETRATION RESISTANCE
   
   Definition: The resistance of soil to deformation or rupture, related to 
   forces binding soil particles together.
   
   Moisture-dependent behavior:
   
   When wet (plasticity, stickiness):
   - Determines ease of root pushing through soil
   - Very plastic soils resist root penetration
   - Sticky soils adhere to roots
   - Can suffocate roots if too wet
   
   When moist (friability):
   - Ideal condition for root growth
   - Soil deforms around growing roots
   - Optimal for root elongation
   - Friable soils best for rooting
   
   When dry (hardness, rigidity):
   - High strength restricts root growth
   - Roots cannot penetrate very hard soils
   - Can prevent seasonal root growth
   - Irrigation or rain needed to soften
   
   Penetration resistance measurement:
   - Measured with penetrometer (MPa or psi)
   - Varies with moisture content
   
   Critical thresholds (at field capacity):
   - <1.0 MPa: No restriction
   - 1.0-2.0 MPa: Slight restriction
   - 2.0-3.0 MPa: Moderate restriction
   - 3.0-4.0 MPa: Severe restriction
   - >4.0 MPa: Root growth prevented
   
   (Note: Thresholds vary by crop species and soil type)


6. SOIL STRUCTURE GRADE AND DEVELOPMENT
   
   Definition: The degree to which structural units (peds) are formed and 
   distinct from adjacent peds.
   
   Structure grades:
   
   Structureless:
   - No observable aggregation
   - Massive or single-grain
   - Poor rooting conditions (if massive)
   - Good rooting if loose (sandy)
   
   Weak structure:
   - Indistinct peds, poorly formed
   - Break into mix of peds and unstructured material
   - Fair rooting conditions
   - Common in subsoils
   
   Moderate structure:
   - Well-formed, fairly distinct peds
   - Durable aggregates
   - Good rooting conditions
   - Roots follow ped faces
   
   Strong structure:
   - Very well-formed, distinct peds
   - Durable, persistent aggregates
   - Excellent rooting conditions
   - Complex pore network


7. CRACKING PATTERNS (Vertic properties)
   
   Definition: Development of deep, wide cracks in shrink-swell soils during 
   dry periods.
   
   Effects on rooting:
   
   Positive aspects:
   - Cracks provide easy pathways for root penetration
   - Self-mulching breaks up surface
   - Deep cracks allow air entry
   - Roots exploit crack networks
   
   Negative aspects:
   - Shrinking can damage roots
   - Cycles of swelling/shrinking stress plants
   - Very high strength when dry between cracks
   - Physical root damage from soil movement
   - Difficult for fine root hairs
   
   Crack characteristics:
   - Width >5 mm significant for rooting
   - Depth >50 cm affects deep rooting
   - Persistence affects crop selection


--------------------------------------
PROFILE CHARACTERISTICS AND HORIZONS
--------------------------------------

Horizon sequence and transitions:

1. ABRUPT TEXTURAL CHANGES
   - Clay increase >20% within 7.5 cm
   - Creates barrier to root penetration
   - Causes perched water tables
   - Common in texture-contrast soils
   - Roots concentrated above boundary

2. GRADUAL TRANSITIONS
   - Favorable for continuous rooting
   - Smooth transition of properties
   - No physical barriers
   - Roots penetrate uniformly

3. LAYERING AND STRATIFICATION
   - Alternating textures or materials
   - Each layer may restrict differently
   - Common in alluvial soils
   - Complex rooting patterns

4. ELUVIAL (E) HORIZONS
   - Often sandy, low bulk density
   - Easy root penetration
   - But may be nutrient-poor
   - Can be droughty

5. ILLUVIAL (Bt, Bk, Bs) HORIZONS
   - Clay, carbonate, or iron accumulation
   - Often denser than overlying horizons
   - Variable rooting depending on development
   - Strong development restricts roots
"""

