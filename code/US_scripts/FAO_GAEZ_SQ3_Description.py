"""
SQ3 RATING SCALE:

Soils are rated on a 0-100 scale based on their rooting conditions:

0-100 SCALE INTERPRETATION:
- 80-100: Very High - Excellent rooting conditions
- 60-79:  High - Good rooting conditions
- 40-59:  Medium - Moderate rooting conditions
- 20-39:  Low - Poor rooting conditions
- 0-19:   Very Low - Very poor rooting conditions


VERY HIGH (80-100): EXCELLENT ROOTING CONDITIONS

Soil characteristics:
- Very deep soil (>150 cm to restrictions)
- Low to moderate bulk density (<1.4 g/cm³ in loams)
- Well-developed, favorable structure (granular, blocky)
- Low mechanical resistance
- No or few coarse fragments (<15%)
- Friable consistence
- Gradual horizon transitions
- No compaction or hardpans

Rooting properties:
- Unrestricted root penetration to great depth
- Uniform root distribution possible
- Easy exploration of large soil volume
- Maximum water and nutrient access
- Strong root anchorage
- No physical stress on roots

Crop performance:
- Suitable for all crop types
- Deepest-rooted crops thrive
- Maximum yield potential
- Excellent drought buffering
- Optimal resource acquisition
- Strong wind resistance

Management advantages:
- Flexible crop selection
- Simple management requirements
- Good response to inputs
- Efficient irrigation use
- Reduced drought risk
- Minimal tillage needed for root conditions


HIGH (60-79): GOOD ROOTING CONDITIONS

Soil characteristics:
- Deep to very deep (100-150 cm)
- Moderate bulk density (1.3-1.5 g/cm³ in loams)
- Moderate to well-developed structure
- Moderate mechanical resistance
- Few to common coarse fragments (15-35%)
- Generally friable consistence
- No severe restricting layers in upper profile
- Minor limitations present

Rooting properties:
- Good root penetration to depth
- Generally uniform root distribution
- Large explorable soil volume
- Good water and nutrient access
- Adequate anchorage
- Minor limitations to deep rooting

Crop performance:
- Suitable for most crops
- May have minor limitations for deepest-rooted perennials
- High yield potential
- Good drought resistance
- Efficient resource use
- Generally strong stands

Management implications:
- Wide crop selection possible
- Standard management practices effective
- Good return on inputs
- Irrigation helpful but not critical in many cases
- May benefit from deep tillage in some situations


MEDIUM (40-59): MODERATE ROOTING CONDITIONS

Soil characteristics:
- Moderately deep (50-100 cm to restrictions)
- Moderate to high bulk density (1.4-1.6 g/cm³ in loams)
- Weak to moderate structure development
- Moderate to high mechanical resistance
- Common coarse fragments (35-60%) or
- Compact subsoil layer, or
- Cemented layer at depth, or
- Abrupt textural discontinuity

Rooting properties:
- Restricted depth of root penetration
- Uneven root distribution
- Limited explorable soil volume
- Reduced water and nutrient access
- Adequate anchorage for most crops
- Physical barriers affect root growth

Crop performance:
- Suitable for shallow to medium-rooted crops
- Limitations for deep-rooted perennials
- Moderate yield potential
- Increased drought susceptibility
- Less efficient resource acquisition
- May require lodging management

Management implications:
- Careful crop selection needed
- Deep tillage may help if restriction near surface
- Increased irrigation requirements
- More frequent but lighter water applications
- Fertilizer placement critical
- May need to supplement with foliar feeding
- Consider raised beds in some cases


LOW (20-39): POOR ROOTING CONDITIONS

Soil characteristics:
- Shallow (25-50 cm to restrictions)
- High bulk density (>1.6 g/cm³ in loams)
- Weak or massive structure
- High mechanical resistance
- Many to abundant coarse fragments (>60%), or
- Shallow restricting layer (hardpan, rock), or
- Very compact or cemented horizons, or
- Very unfavorable structure (massive, platy)

Rooting properties:
- Severely restricted rooting depth
- Highly uneven root distribution
- Very limited explorable soil volume
- Poor water and nutrient access
- Inadequate anchorage for many crops
- Severe physical barriers
- Roots concentrated in limited zones

Crop performance:
- Only shallow-rooted crops suitable
- Severe limitations for most crops
- Low yield potential without intensive management
- High drought susceptibility
- Very inefficient resource use
- Lodging common
- Stunted growth typical

Management requirements:
- Very limited crop selection
- Intensive management essential
- Frequent irrigation required
- Light, frequent applications needed
- Specialized equipment may be needed
- Soil modification may be necessary (deep ripping, rock removal)
- May only be viable for high-value specialty crops
- Consider alternative land uses


VERY LOW (0-19): VERY POOR ROOTING CONDITIONS

Soil characteristics:
- Very shallow (<25 cm to restrictions)
- Very high bulk density (>1.7 g/cm³)
- Structureless, massive
- Very high mechanical resistance
- Dominant coarse fragments (>90%), or
- Rock outcrop, or
- Bedrock at or near surface, or
- Extremely compacted or cemented layers, or
- Continuous hardpan

Rooting properties:
- Rooting essentially prevented or minimal
- No effective root system possible
- Extremely limited soil volume
- Minimal water and nutrient access
- No anchorage
- Severe to absolute physical barriers

Crop suitability:
- Unsuitable for conventional agriculture
- Only most specialized plants possible
- Extremely low productivity
- Not economically viable for most uses

Alternative uses:
- Forestry (limited species)
- Extensive grazing (poor quality)
- Wildlife habitat
- Conservation/preservation
- May require complete soil reconstruction for agriculture
"""

# ============================================================================
# RATING METHODOLOGY
# ============================================================================

"""
APPROACH TO RATING SQ3:

The rating methodology uses a "most limiting factor" approach with adjustments 
for multiple factors:

1. PRIMARY RATING FACTORS:

   Effective Soil Depth:
   - Single most important factor
   - Weighted 40-50% of total rating
   - Absolute limitation if very shallow
   - Evaluated to 150 cm or restricting layer
   
   Bulk Density/Compaction:
   - Weighted 20-30% of rating
   - Evaluated by depth increment
   - Critical in subsoil
   - Strong interaction with texture
   
   Soil Structure:
   - Weighted 15-25% of rating
   - More important in fine-textured soils
   - Less critical in coarse soils
   - Quality varies by horizon


2. MODIFIER FACTORS:

   Coarse Fragment Content:
   - Modifies effective rooting volume
   - Reduces available water capacity
   - High content severely limits rating
   
   Textural Class and Changes:
   - Affects all other factors
   - Abrupt changes reduce rating
   - Very fine textures can restrict roots
   
   Penetration Resistance:
   - Direct measure when available
   - Correlates with bulk density and structure
   - Moisture-dependent


3. PROFILE INTEGRATION:

   Depth-weighted evaluation:
   - 0-30 cm (topsoil): 30-40% of rating
   - 30-60 cm (upper subsoil): 25-30% of rating
   - 60-100 cm (lower subsoil): 20-25% of rating
   - 100-150 cm (deep subsoil): 10-15% of rating
   
   Rationale:
   - All crops root in topsoil
   - Most root activity in upper 60 cm
   - Deep rooting provides major benefits
   - Subsoil increasingly important in dry climates


4. MOST LIMITING FACTOR EMPHASIS:

   Single severe limitation caps overall rating:
   - Bedrock at 20 cm → maximum rating ~30 regardless of other factors
   - Hardpan at 40 cm → maximum rating ~45
   - Very high bulk density throughout → maximum rating ~40
   
   Multiple moderate limitations:
   - Cumulative effect reduces rating
   - Each additional limitation has impact
   - Combined effects often multiplicative


5. SPECIAL CONSIDERATIONS:

   Cracking clay soils (Vertisols):
   - Cracks improve access when dry
   - But swelling/shrinking stresses roots
   - Variable rating depending on climate
   - Management-dependent
   
   Volcanic soils (Andisols):
   - Often excellent structure
   - Low bulk density naturally
   - High ratings common
   - May have buried horizons
   
   Sandy soils (Psamments):
   - Low mechanical resistance
   - But single-grain structure
   - Often deep but low water retention
   - Rated moderate to high for rooting per se
   
   Organic soils (Histosols):
   - Excellent when drained
   - Very poor if waterlogged
   - Variable rating with water table


6. CROP-SPECIFIC ADJUSTMENTS:

   Shallow-rooted crops (0-50 cm):
   - Only evaluate upper profile
   - Restrictions below 50 cm less important
   - Higher ratings possible with shallow restrictions
   
   Medium-rooted crops (50-100 cm):
   - Standard evaluation depth
   - Restrictions at 80-100 cm moderate impact
   
   Deep-rooted crops (>100 cm):
   - Full profile critical
   - Restrictions at any depth important
   - Lower ratings with any deep limitations


7. CLIMATIC CONTEXT:

   Humid regions:
   - Deep rooting less critical
   - Surface layers more important
   - Structure more important than depth
   
   Arid/semi-arid regions:
   - Deep rooting highly critical
   - Access to deep moisture essential
   - Depth weighted more heavily
   - Restrictions more severely penalized
   
   Irrigated systems:
   - Depth less critical
   - Upper profile most important
   - Frequent water reduces depth need
"""

